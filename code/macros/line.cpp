#include <iostream>

// #line 100 "custom_file.cpp": This directive changes the compiler's internally stored line number and optionally the filename.
// In this case, it sets the current line number to 100 and the filename to "custom_file.cpp".
// This can be useful for customizing error messages and debugging information.

#line 100 "custom_file.cpp"

int main() {
    // This line will appear as line 100 in the file custom_file.cpp during compilation.
    std::cout << "This line will appear as line 100 in the file custom_file.cpp during compilation." << std::endl;
    return 0;
}

