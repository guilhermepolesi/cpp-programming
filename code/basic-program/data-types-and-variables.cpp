#include <iostream>
#include <string>

using namespace std;

int main() {

    int num = 10;
    cout << "Integer variable: " <<  num << endl;

    float floating_num = 3.1415;
    cout << "Simple floating point: " << floating_num << endl;

    double larger_num = 99999999999999.99999999;
    cout << "Largest floating point: " << larger_num << endl;

    char character = 'A';
    cout << "The character is: " << character << endl;
    
    string name = "Bjarne Stroustrup";
    cout << "The name of the creator of C++ is: " << name << endl;

    return 0;

}
